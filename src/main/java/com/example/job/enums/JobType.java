package com.example.job.enums;

/***
 * Класс Enum для определения типа работы для компании.
 * **/
public enum JobType {
    /**
     * Определяет тип работы на полный рабочий день
     **/
    FULL_TIME, /**
     * Определяет тип контрактной работы
     **/
    CONTRACT, /**
     * Определяет тип работы с частичной занятостью.
     **/
    PART_TIME
}
